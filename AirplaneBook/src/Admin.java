public class Admin{
    String adminID;
    String adminPwd;

    public Admin(String adminID, String adminPwd){
        this.adminID = adminID;
        this.adminPwd = adminPwd;
    }

    public String getAdminID() {
        return adminID;
    }
    public String getAdminPwd() {
        return adminPwd;
    }

}
